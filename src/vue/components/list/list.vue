<template>
  <div class="sv-list__container">
    <div class="sv-list__filter" v-if="model.showFilter">
      <div class="sv-list__filter-icon">
        <sv-svg-icon :iconName="'icon-search'" :size="'auto'"> </sv-svg-icon>
      </div>
      <input
        type="text"
        class="sv-list__input"
        :aria-label="model.filterStringPlaceholder"
        :placeholder="model.filterStringPlaceholder"
        :value="model.filterString"
        @change="change"
        @keyup="keyup"
      />
    </div>
    <div class="sv-list__empty-container" v-if="model.isEmpty">
      <div class="sv-list__empty-text" :aria-label="model.emptyMessage">{{ model.emptyMessage }}</div>
    </div>
    <ul
      class="sv-list"
      role="listbox"
      @mousedown="
        (event) => {
          event.preventDefault();
        }
      "
      v-on:keydown="
        (event) => {
          model.onKeyDown(event);
        }
      "
    >
      <sv-list-item
        v-for="item in model.renderedActions"
        :item="item"
        :model="model"
        :key="item.id"
      >
      </sv-list-item>
    </ul>
  </div>
</template>

<script lang="ts">
import Vue from "vue";
import { Component, Prop } from "vue-property-decorator";
import { ListModel } from "survey-core";
import { BaseVue } from "../../base";

export * from "./list-item.vue";

@Component
export class List extends BaseVue {
  @Prop() model: ListModel;
  constructor() {
    super();
  }
  getModel() {
    return this.model;
  }
  change(event: any) {
    this.model.filterString = event.target.value;
  }
  keyup(event: any) {
    this.model.filterString = event.target.value;
    this.model.goToItems(event);
  }
}

Vue.component("sv-list", List);
export default List;
</script>
